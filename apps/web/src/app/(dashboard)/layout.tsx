function useCurrentUser() {
  const [user, setUser] = useState<{ role: string; allowedModules?: string[] } | null>(null);
  useEffect(() => {
    const decoded = getUser();
    if (decoded) {
      setUser({ role: decoded.role });
    }
  });
  return user;
}

function canAccessModule(user: { role: string; allowedModules?: string[] } | null, moduleId: string): boolean {
  if (!user) return false;
  if (user.role === 'SUPER_ADMIN') return true;
  // Custom overrides from user.allowedModules take priority
  if (user.allowedModules && user.allowedModules.length > 0) {
    return user.allowedModules.includes(moduleId);
  }
  // Default permissions from role
  const defaults = MODULE_PERMISSIONS[user.role] || [];
  return defaults.includes('*') || defaults.includes(moduleId);
}

